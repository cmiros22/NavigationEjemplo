package com.example.navigationejemplo.Entidades;

public class Usuarios {
    String Usuario;
    String Password;

    public Usuarios(){

    }

    public String getUsuario() {
        return Usuario;
    }

    public void setUsuario(String usuario) {
        Usuario = usuario;
    }

    public String getPassword() {
        return Password;
    }

    public void setPassword(String password) {
        Password = password;
    }
}
